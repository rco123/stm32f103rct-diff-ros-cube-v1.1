int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM8_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_USART1_UART_Init();
  /* USER CODE BEGIN 2 */

  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim8, TIM_CHANNEL_2);


  TIM8->CCR1 = 00;
  TIM8->CCR2 = 00;

  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_4, GPIO_PIN_SET);
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_5, GPIO_PIN_SET);


  HAL_TIM_Encoder_Start(&htim2, TIM_CHANNEL_ALL);
  HAL_TIM_Encoder_Start(&htim3, TIM_CHANNEL_ALL);


  //Motor 1 round count
  //GA25-370 RPM 60 --> ratio 103, count = 103 * 11 * 4 = 4532
  //GA25-370 RPM 77 --> ratio 78, count = 78 * 11 * 4 = 3432
  //GA25-370 RPM 130 --> ratio 46, count = 46 * 11 * 4 = 2024

  // Wheel Round Length
  // 2* PI * r(radius) = 2 * 3.14159 * ( 65mm /2 ) ,  =  204.2 (~~> 204mm )


  // speed
  // case GA25-370 RPM 77:
  // speed = S / T, UNIT: mm
  // encode_cnt / 2432 * (204.2 mm) / (10 * 1e-3 )


  //PID(&TPID, &speed_t2, &PIDOut,  &speed_set_point, 2, 5, 1, _PID_P_ON_E, _PID_CD_DIRECT);

  PID(&TPID, &speed_t2, &PIDOut,  &speed_set_point, 4.5, 1.2, 0.1, _PID_P_ON_E, _PID_CD_DIRECT);


  PID_SetMode(&TPID, _PID_MODE_AUTOMATIC);
  PID_SetSampleTime(&TPID, 10);
  PID_SetOutputLimits(&TPID, -900, 900);


  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  uint32_t now = 0;
  uint32_t last_time = 0;
  uint32_t time_change = 0 ;


  //MOTOR 2
  TIM8->CCR1 = 450;
  //HAL_GPIO_WritePin(GPIOC, GPIO_PIN_4, GPIO_PIN_RESET);  // BACK
  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_4, GPIO_PIN_SET);  // FORTH
  speed_set_point = 20;


//  TIM8->CCR2 = 00;
//  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_5, GPIO_PIN_SET);


  while(1)
  {
	  TIM8->CCR1 = 0;
	  TIM8->CCR2 = 0;
  }


  while (1)
  {

	  //now = GetTime();

	  now = HAL_GetTick(); //msec, 1e-3
	  time_change = (now - last_time);


	  if(time_change >= TPID.SampleTime)
	  {
		  g_encoder_cnt_t2 = (short)TIM2 -> CNT; TIM2 -> CNT = 0x0000;
	  	  g_encoder_cnt_t3 = (short)TIM3 -> CNT; TIM3 -> CNT = 0x0000;

	  	  g_encoder_cnt_t2 = g_encoder_cnt_t2 * (-1); //DIR REVERSE COUNT

	  	  g_tot_enc_cnt_t2 += g_encoder_cnt_t2;
	  	  g_tot_enc_cnt_t3 += g_encoder_cnt_t3;


	  	  // speed
	  	  // case GA25-370 RPM 77:
	  	  // speed = S / T, UNIT: mm
	  	  // encode_cnt / 2432 * (204.2 mm) / (10 * 1e-3 )

	  	  speed_t2 = g_encoder_cnt_t2 / 2432.0 * (204.2) / (time_change * 1e-3 );
	  	  speed_t3 = g_encoder_cnt_t3 / 2432.0 * (204.2) / (time_change * 1e-3);


	  	  PID_Compute(&TPID);

	  	  if( PIDOut >= 0)
	  	  {
	  	  	  HAL_GPIO_WritePin(GPIOC, GPIO_PIN_4, GPIO_PIN_SET);  // FORTH
	  	  	  g_pwm = (uint32_t) PIDOut;
	  	  	  //g_pwm =    (uint32_t) TPID.MyOutput;
	  		  TIM8->CCR1 = g_pwm;
	  	  }
	  	  else if( PIDOut < 0 ){
	  		HAL_GPIO_WritePin(GPIOC, GPIO_PIN_4, GPIO_PIN_RESET);  // BACK
	  		g_pwm = (uint32_t) (PIDOut * -1);
	  		//g_pwm =  (uint32_t) ( (int)TPID.MyOutput * -1 );
	  		TIM8->CCR1 = g_pwm;
	  	  }

	  	  last_time = now;

	  }


    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

